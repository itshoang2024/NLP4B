# ── 3. Data structures ───────────────────────────────────────────────────────
class UploadTask(NamedTuple):
    """Represents a single file that needs to be uploaded."""
    local_path: Path
    container: str
    blob_path: str  # e.g. "video_id/filename.jpg"


